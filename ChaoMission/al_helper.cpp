char* DecodeChaoName(char Name[])
{
	int i = 0;
	char result[7];
	while (Name[i] != 0 && i < 7)
	{
		if (Name[i] < 256) result[i] = ChaoNameDecoder[Name[i]];
		else result[i] = '_';
		i++;
	}
	return result;
}

